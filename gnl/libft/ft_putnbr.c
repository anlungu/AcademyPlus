#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned char	c;
	long			a;

	a = n;
	if (a < 0)
	{
		a *= -1;
		write(1, "-", 1);
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	if (a < 10 && a >= 0)
	{
		c = (unsigned char)a + '0';
		write(1, &c, 1);
	}
}
