#include "libft.h"

int		ft_sqrt(int nb)
{
	int sqr;

	if (nb)
	{
		sqr = 1;
		while ((sqr != (nb / sqr)) && (sqr <= (nb / 2)))
			sqr++;
		if (sqr * sqr == nb)
			return (sqr);
		else
			return (0);
	}
	return (0);
}
