#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	char	*new;

	if ((!s1 || !s2) || !(new = ft_strnew(ft_strlen(s1) + ft_strlen(s2))))
		return (NULL);
	ft_strcpy(new, s1);
	ft_strcat(new, s2);
	return (new);
}
